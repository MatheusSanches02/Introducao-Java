public class Operadores {
    public static void main(String[] args){
        int numeroA = 15;
        System.out.println("Numero A: " + numeroA);

        int numeroB = 25;
        System.out.println("Numero B: " + numeroB);

        int soma = numeroA + numeroB;
        System.out.println("O resultado da soma eh:  " + soma);

        int subtracao = soma - 10;
        System.out.println("A subtração por 10 eh : " + subtracao);

        int multiplicacao = subtracao * 5;
        System.out.println("A multiplicação por 5 eh: " + multiplicacao);

        int divisao = multiplicacao / 2;
        System.out.println("A divisao por 2 eh: " + divisao);
        
    }
    
}
